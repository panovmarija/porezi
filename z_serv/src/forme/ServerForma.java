    public void osvezi() {
        List<Obaveznik>l=kontroer.Kontroler.getInstance().vratiPodneli();
        List<Obaveznik>n=new LinkedList<>();
        for(Obaveznik o:l)
        {
        if(sip!=null && sjmbg!=null)
        {
            if(o.getJmbg().equals(sjmbg) && (o.getIme()+" "+o.getPrez()).equalsIgnoreCase(sip))
                n.add(o);
        }
        else if(sip!=null)
        {
            if((o.getIme()+" "+o.getPrez()).equalsIgnoreCase(sip))
                n.add(o);
        }
        else if(sjmbg!=null)
        {
            if(o.getJmbg().equals(sjmbg) )
                n.add(o);
        }
        else n.add(o);
        }
        ms=new ModelServ(n);
        jTable1.setModel(ms);
    }
